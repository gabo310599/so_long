#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	struct s_list	*ptr;

	ptr = lst;
	while (ptr)
	{
		if (!ptr->next)
			return (ptr);
		ptr = ptr->next;
	}
	return (ptr);
}
